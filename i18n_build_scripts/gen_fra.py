for k, v in new_translations.items():
    if k not in merged:
        merged[k] = v

# Verify all EN keys present
missing = set(en_dict.keys()) - set(merged.keys())
if missing:
    print(f"ERROR: Still missing {len(missing)} keys:")
    for k in sorted(missing):
        print(f"  {repr(k)}")
    exit(1)

# Sort keys
sorted_keys = sorted(merged.keys(), key=str.lower)

# Generate output
lines = ['"""Traductions françaises (French) — toutes les clés."""', '', 'from __future__ import annotations', '', 'TRANSLATIONS: dict[str, str] = {']

"""Generate 7 translation files from EN master."""
import re, os, ast, sys

EN_PATH = r"V:\___VAC\__K\__CODE\_PY\_FastPrompter\src\fastprompter\core\i18n\en.py"
OUT_DIR = r"V:\___VAC\__K\__CODE\_PY\_FastPrompter\src\fastprompter\core\i18n"

with open(EN_PATH, encoding="utf-8") as f:
    src = f.read()

start = src.index("TRANSLATIONS: dict[str, str] = {") + len("TRANSLATIONS: dict[str, str] = {")
depth, i = 1, start
while depth > 0 and i < len(src):
    if src[i] == "{": depth += 1
    elif src[i] == "}": depth -= 1
    i += 1
dict_text = src[start:i-1]

# Parse using ast
tree = ast.parse(f"_d = {{{dict_text}}}")
keys_ast = tree.body[0].value
entries = []
for k, v in zip(keys_ast.keys, keys_ast.values):
    entries.append((k.value, v.value))
print(f"Parsed {len(entries)} EN keys")

def esc(s, q="'"):
    s = s.replace("\\", "\\\\").replace("\n", "\\n").replace("\t", "\\t")
    return s.replace("\\'", "'").replace("'", "\\'") if q == "'" else s.replace('\\"', '"').replace('"', '\\"')

def write_file(lang_code, header, trans_dict):
    lines = []
    lines.append(f'"""{header} — {len(trans_dict)} keys."""')
    lines.append('')
    lines.append('from __future__ import annotations')
    lines.append('')
    lines.append('TRANSLATIONS: dict[str, str] = {')
    for k, v in sorted(trans_dict.items(), key=lambda x: x[0].lower()):
        has_sq = "'" in v
        if has_sq:
            ek = esc(k, '"')
            ev = esc(v, '"')
            lines.append(f'    "{ek}": "{ev}",')
        else:
            ek = esc(k, "'")
            ev = esc(v, "'")
            lines.append(f"    '{ek}': '{ev}',")
    lines.append('}')
    lines.append('')
    path = os.path.join(OUT_DIR, lang_code)
    with open(path, "w", encoding="utf-8", newline="") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  {lang_code}: {len(trans_dict)} keys")

def make_dict(overrides):
    d = {}
    for k, v in entries:
        d[k] = overrides.get(k, v)
    return d


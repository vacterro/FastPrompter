# Sort by escaped representation to match file-on-disk order
def escape_key(k):
    return k.replace('\\', '\\\\').replace('\n', '\\n').replace('\t', '\\t').replace('"', '\\"')

sorted_keys = sorted(all_translations.keys(), key=escape_key)

lines = []

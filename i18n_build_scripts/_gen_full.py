lines.append('')
lines.append('from __future__ import annotations')
lines.append('')
lines.append('TRANSLATIONS: dict[str, str] = {')

for key in sorted_keys:
    val = all_translations[key]
    # Escape for Python source: backslash first, then newline/tab, then double-quote
    escaped_key = escape_key(key)
    escaped_val = val.replace('\\', '\\\\').replace('\n', '\\n').replace('\t', '\\t').replace('"', '\\"')
    lines.append(f'    "{escaped_key}": "{escaped_val}",')

lines.append('}')

output = '\n'.join(lines) + '\n'


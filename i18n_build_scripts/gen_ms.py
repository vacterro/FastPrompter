def write_file():
    lines = []
    lines.append('"""Bahasa Melayu (Malay) \u2014 483 kunci."""')
    lines.append('')
    lines.append('from __future__ import annotations')
    lines.append('')
    lines.append('TRANSLATIONS: dict[str, str] = {')
    for k, v in en_entries:
        tv = MS.get(k, v)
        k_repr = repr(k)
        v_repr = repr(tv)
        lines.append(f'    {k_repr}: {v_repr},')
    lines.append('}')
    
    path = os.path.join(d, 'ms.py')
    content = '\n'.join(lines) + '\n'
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    cnt = sum(1 for l in content.split('\n') if l.strip().startswith("'") and ':' in l)
    print(f'ms.py: {cnt} entries ({len(MS)} in MS dict)')

